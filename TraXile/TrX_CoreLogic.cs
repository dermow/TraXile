            if (enteringDefaultTrackableActivity)
            {
                if (_currentActivity == null)
                {
                    _currentActivity = new TrX_TrackedActivity
                    {
                        Area = sTargetArea,
                        Type = actType,
                        AreaLevel = _nextAreaLevel,
                        Started = ev.EventTime,
                        TimeStamp = lTS,
                        InstanceEndpoint = _currentInstanceEndpoint,
                    };
