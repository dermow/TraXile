                    || _defaultMappings.TimelessLegionAreas.Contains(sSourceArea)
                    || _defaultMappings.TrialMasterAreas.Contains(sSourceArea)
                    || _defaultMappings.LakeOfKalandraAreas.Contains(sSourceArea);

