            // One Area + Own instance
            bool enteringDefaultTrackableActivity =
                bTargetAreaIsMap ||
                bTargetAreaIsHeist ||
                bTargetAreaIsSimu ||
                bTargetAreaIsLab ||
                (bTargetAreaIsSanctum && isOutsideSanctum) ||
                bTargetAreaMine ||
                bTargetAreaIsToTa ||
                bTargetAreaIsTane ||
                bTargetAreaTemple ||
                bTargetAreaIsMI ||
                bTargetAreaIsUberAtziri ||
                bTargetAreaIsAtziri ||
                bTargetAreaIsElder ||
                bTargetAreaIsShaper ||
                bTargetAreaIsMavenFight ||
                bTargetAreaIsSirusFight ||
                bTargetAreaIsLogbook ||
                bTargetAreaIsSafehouse ||
                bTargetAreaIsCata ||
                bTargetAreaIsExarch ||
                bTargetAreaIsBreachStone ||
                bTargetAreaIsBlackStar ||
                bTargetAreaIsEaterOfWorlds ||
                bTargetAreaIsInfinitetHunger ||
                bTargetAreaIsLegion ||
                bTargetAreaIsTrialmaster ||
                bTargetAreaIsKalandra;

            // Check if opened activity needs to be opened on Mapdevice
            bool isMapDeviceActivity =
                bTargetAreaIsMap ||
                bTargetAreaIsAtziri ||
                bTargetAreaIsCata ||
                bTargetAreaIsElder ||
                bTargetAreaIsShaper ||
                bTargetAreaIsSimu ||
                bTargetAreaIsSafehouse ||
                bTargetAreaIsSirusFight ||
                bTargetAreaTemple ||
                bTargetAreaIsMI ||
                bTargetAreaIsMavenFight ||
                bTargetAreaIsLogbook ||
                bTargetAreaIsExarch ||
                bTargetAreaIsBreachStone ||
                bTargetAreaIsBlackStar ||
                bTargetAreaIsInfinitetHunger ||
                bTargetAreaIsEaterOfWorlds ||
                bTargetAreaIsLegion ||
                bTargetAreaIsTrialmaster ||
                bTargetAreaIsKalandra;

            if (isMapDeviceActivity)
            {
                if (!bTargetAreaIsShaper)
                {
                    _shaperKillsInFight = 0;
                }

                if (!bTargetAreaIsElder)
                {
                    _lastElderInstance = "";
                }
